def draw_char(ch, x, y, scale=1.5):
    data = FONT.get(ch.upper(), FONT.get(' '))
    if data is None:
        return
    glBegin(GL_POINTS)
    for row, bits in enumerate(data):
        for col in range(5):
            if bits & (0x10 >> col):
                glVertex2f(x + col * scale, y - row * scale)
    glEnd()

def draw_text(text, x, y, scale=1.5):
    glColor3f(1.0, 1.0, 1.0)
    glPointSize(2.0)
    cx = x
    for ch in text:
        draw_char(ch, cx, y, scale)
        cx += 7 * scale

def draw_text_colored(text, x, y, color, scale=1.5):
    glColor3f(*color)
    glPointSize(2.0)
    cx = x
    for ch in text:
        draw_char(ch, cx, y, scale)
        cx += 7 * scale

def draw_circle(radius, filled=True):
    if filled:
        glBegin(GL_TRIANGLE_FAN)
        glVertex2f(0, 0)
        for i in range(101):
            angle = 2 * math.pi * i / 100
            glVertex2f(radius * math.cos(angle), radius * math.sin(angle))
        glEnd()
    else:
        glBegin(GL_LINE_LOOP)
        for i in range(100):
            angle = 2 * math.pi * i / 100
            glVertex2f(radius * math.cos(angle), radius * math.sin(angle))
        glEnd()

def draw_stars():
    glColor3f(1.0, 1.0, 1.0)
    glPointSize(1.5)
    glBegin(GL_POINTS)
    for (x, y) in stars:
        glVertex2f(x, y)
    glEnd()

def draw_sun():
    glColor3f(1.0, 0.9, 0.0)
    draw_circle(40)

def draw_orbit(radius):
    glColor3f(0.25, 0.25, 0.25)
    draw_circle(radius, filled=False)

def draw_planets():
    selected_name = planet_names[selected_index]
    for name, (color, orbit_r, size, orbit_speed) in planets.items():
        draw_orbit(orbit_r)

        rad = math.radians(angles[name])
        px = orbit_r * math.cos(rad)
        py = orbit_r * math.sin(rad)

        glPushMatrix()
        glRotatef(angles[name], 0, 0, 1)
        glTranslatef(orbit_r, 0, 0)
        scale = planet_scales[name]
        glScalef(scale, scale, 1.0)
        glColor3f(*color)
        draw_circle(size)
        glPopMatrix()

        if name == selected_name:
            draw_text_colored(name.upper(), px + size + 3, py + size + 8, (1.0, 1.0, 0.0))
        else:
            draw_text(name.upper(), px + size + 3, py + size + 8)

def update():
    for name, (color, orbit_r, size, orbit_speed) in planets.items():
        angles[name] += orbit_speed * speed

running = True
while running:
    for event in pygame.event.get():
        if event.type == QUIT:
            running = False
        if event.type == KEYDOWN and event.key == K_ESCAPE:
            running = False

    glClear(GL_COLOR_BUFFER_BIT)
    glLoadIdentity()

    update()
    draw_stars()
    draw_sun()
    draw_planets()

    pygame.display.flip()
    pygame.time.wait(16)

